package com.dkl.merchantdb.to;

public class ItemLedgerTO {
	
	private String companyID;
	private String itemID;
	private String itemName;
	private String ledgerGroupName;
	private String itemLedgerID;
	private String itemLedgerName;
	private String creationDate;
	private String modifiedDate;
	public String getCompanyID() {
		return companyID;
	}
	public void setCompanyID(String companyID) {
		this.companyID = companyID;
	}
	public String getItemID() {
		return itemID;
	}
	public void setItemID(String itemID) {
		this.itemID = itemID;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getLedgerGroupName() {
		return ledgerGroupName;
	}
	public void setLedgerGroupName(String ledgerGroupName) {
		this.ledgerGroupName = ledgerGroupName;
	}
	public String getItemLedgerID() {
		return itemLedgerID;
	}
	public void setItemLedgerID(String itemLedgerID) {
		this.itemLedgerID = itemLedgerID;
	}
	public String getItemLedgerName() {
		return itemLedgerName;
	}
	public void setItemLedgerName(String itemLedgerName) {
		this.itemLedgerName = itemLedgerName;
	}
	public String getCreationDate() {
		return creationDate;
	}
	public void setCreationDate(String creationDate) {
		this.creationDate = creationDate;
	}
	public String getModifiedDate() {
		return modifiedDate;
	}
	public void setModifiedDate(String modifiedDate) {
		this.modifiedDate = modifiedDate;
	}
	
	
	
}
